//buscar temas
function buscarTemas(event) {
    event.preventDefault();
    let busqueda = document.getElementById('inputBusqueda').value;
//limpiar los resultados de la busqueda anterior
    document.getElementById('inputBusqueda').value = '';

// Variable para comprobar si se encontraron coincidencias
    var encontrado = false;

    for (let i = 0; i < discos.length; i++) {
        let tema = discos[i];
        //buscar coincidencia de lo que busco
        if(tema.trackName.toLowerCase().includes(busqueda.toLowerCase()) || tema.artist.toLowerCase().includes(busqueda.toLowerCase()) ){            
            let resultadoCancion = document.createElement('p');
            resultadoCancion.textContent = ` ${tema.trackName}` ;

            //que pasa si hay dos coincidemas como por ej: Verano del 92
            // como redireccionar en swal un link del ob

            Swal.fire({
                // titleText: busqueda,
                titleText: `Cancion: ${tema.trackName}`,
                text: `${tema.artist} - ${tema.album}`,
                    imageUrl: `${tema.albumCover}`,
                    ImageWidth: '50px',

                // html:
                //   '<a href="./disco3.html">' +
                //   'Ir al disco',
                // showCloseButton: true,
                // focusConfirm: false,

                showCancelButton: true,
            //     confirmButtonText: 'Sí',
            //     cancelButtonText: 'No'
            //   }).then((result) => {
            //     if (result.isConfirmed) {
            //       // Redireccionar a la URL almacenada en la propiedad "ref"
            //       window.location.href = discos.ref;
            //     }
              });
// Cambiar la variable a true para indicar que se encontraron coincidencias
          encontrado = true;
        }
    }
    if (!encontrado) {
        Swal.fire({
            icon: 'error',
            title: 'Oops...',
            text: 'No se encontraron coincidencias.',
          })
      }
}
document.getElementById('btnBuscar').addEventListener('click', buscarTemas)
